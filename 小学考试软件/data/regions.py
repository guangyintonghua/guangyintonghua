def get_exam_context(region: str = "全国通用", textbook: str = "人教版（PEP）") -> str:
    """返回注入 AI 出题提示词的地区大纲 + 教材版本说明文本。"""
    parts: list[str] = []

    if region and region != "全国通用":
        info = REGION_INFO.get(region)
        if info:
            block = [f"【地区出题要求】严格按照{region}小学数学考试大纲出题"]
            block.append(f"出题风格：{info['exam_style']}")
            block.append(f"重点考查方向：{info['focus']}")
            if info.get("notes"):
                block.append(f"注意事项：{info['notes']}")
            parts.append("\n".join(block))
        else:
            parts.append(f"【地区出题要求】按照{region}小学数学考试大纲和出题惯例出题")

    tb_desc = TEXTBOOKS.get(textbook, "")
    if textbook and textbook != "人教版（PEP）":
        parts.append(f"【教材版本】{textbook}\n说明：{tb_desc}")
    elif textbook == "人教版（PEP）" and not parts:
        pass  # default, no need to add noise
    elif tb_desc:
        parts.append(f"【教材版本】{textbook}\n说明：{tb_desc}")

    return "\n\n".join(parts)
