def get_topics_for_grade(grade: str) -> dict:
    return KNOWLEDGE_TREE.get(grade, {})


def get_all_kp_for_grade(grade: str) -> list[str]:
    """返回某年级所有知识点的平铺列表"""
    result = []
    for semester_data in KNOWLEDGE_TREE.get(grade, {}).values():
        for unit, kps in semester_data.items():
            result.extend(kps)
    return result


def get_units_for_grade_semester(grade: str, semester: str) -> dict:
    return KNOWLEDGE_TREE.get(grade, {}).get(semester, {})


